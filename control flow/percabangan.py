# if elif else
nilai = 1
perilaku = "buruk"
if perilaku == "baik":
    if nilai >= 90:print("A+")
    elif nilai >= 80:print("b+")
    elif nilai >= 72:print("c+")
    elif nilai >= 60:print("d+")
    else:print("goublok tapi atitud apik yo wes rapopo")
elif perilaku != "baik":
    if nilai >= 90:print("A-")
    elif nilai >= 80:print("b-")
    elif nilai >= 72:print("c-")
    elif nilai >= 60:print("d-")
    else:print("wes goublok atitud elek wes rasah sekolah le...")

# Ternary Operator
